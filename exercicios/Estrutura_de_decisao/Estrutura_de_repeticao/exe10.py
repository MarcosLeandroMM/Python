# Básico - Soma de números: Peça ao usuário para inserir uma sequência de números e, em seguida, calcule e imprima a soma desses números.

num1 = int(input("Digite um número: "))
num2 = int(input("Digite um segundo número: "))
num3 = int(input("Mais um número: "))

soma_dos_numeros = num1 + num2 + num3

print(f"A soma dos números digitados é {soma_dos_numeros}")